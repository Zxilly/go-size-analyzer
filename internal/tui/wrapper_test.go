func Test_wrapper_FileTitleUsesPlaceholderForUnknownSourceFile(t *testing.T) {
	w := newWrapper(&entity.File{})

	assert.Equal(t, "(unknown source)", w.Title())
	assert.Contains(t, w.Description(), "# (unknown source) _(File)_")
	assert.Contains(t, w.Description(), "- **Path:** (unknown source)")
}

func Test_wrapper_MethodDescriptionTitleIncludesReceiver(t *testing.T) {
	w := newWrapper(&entity.Function{
		Name:     "ServeHTTP",
		Type:     entity.FuncTypeMethod,
		Receiver: "Handler",
	})

	assert.Contains(t, w.Description(), "# Handler.ServeHTTP _(Function)_")
}

